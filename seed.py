    db.session.commit()
    print(f"✅ {added} uçuş eklendi!\n")

    by_route = {}
    for fl in Flight.query.all():
        key = f"{fl.airport_from}→{fl.airport_to}"
        by_route[key] = by_route.get(key, 0) + 1

    print("📊 Güzergah özeti:")
    for route, count in sorted(by_route.items()):
        print(f"  {route}: {count} uçuş")
