def make_flights(fn_prefix, airport_from, airport_to, duration, capacity, dates_times):
    flights = []
    counter = 1
    for date, times in dates_times:
        for dep_time, arr_time in times:
            flights.append({
                "flight_number": f"{fn_prefix}{counter:02d}",
                "date_from": f"{date}T{dep_time}:00",
                "date_to":   f"{date}T{arr_time}:00",
                "airport_from": airport_from,
                "airport_to":   airport_to,
                "duration": duration,
                "capacity": capacity
            })
            counter += 1
    return flights

